
class Calculadora:
    def __init__(self, a=None, b=None,):
        self.a = a  
        self.b = b
        
    def soma(self, a, b):
        self.a = a
        self.b = b
        return self.a + self.b
       
    def subtracao(self, a, b):
        self.a = a
        self.b = b
        return self.a - self.b


    def multiplicacao(self, a, b):
        self.a = a
        self.b = b
        return self.a * self.b


    def divisao(self, a, b):
        self.a = a
        self.b = b
        return self.a / self.b
        

